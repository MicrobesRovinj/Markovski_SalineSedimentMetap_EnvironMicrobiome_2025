#################################################################################################################
# p_values_to_labels.R
#
# Function to represent p-values using asterisks.
#
#################################################################################################################

p_values_to_labels <- function(x = NULL) {
  
  case_when(x < 0.05 & x >= 0.01 ~ "*",
            x < 0.01 & x >= 0.001 ~ "**",
            x < 0.001 & x >= 0.0001 ~ "***",
            x < 0.0001 ~ "****",
            TRUE ~ "NS")
  
}
